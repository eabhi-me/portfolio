            this.setLeetCodeFallback()
          }
        } else {
          this.setLeetCodeFallback()
        }
      } catch (error) {
        console.error('Error fetching LeetCode data (CORS):', error)
        // Use realistic fallback data based on actual profile
        this.setLeetCodeFallback()
      } finally {
        this.codingProfiles.leetcode.loading = false
      }
